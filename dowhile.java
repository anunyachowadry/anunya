// Java Program to Illustrate Do-while Loop

// Class
public class dowhile{

    // Main driver method
    public static void main(String args[]) {

        // Declaring and initialization expression
        int i = 1;

        // Do-while loop
        do {

            // Body of do-while loop
            // Print statement
            System.out.println("anunya");

            // Update expression
            i++;
        }

        // Test expression
        while (i < 6);
    }
}


